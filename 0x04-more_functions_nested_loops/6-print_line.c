#include "main.h"

/**
 * print_line - this is a function to print a straight line
 * @n: straight line parameter
 *
 * Return: 0
 */

void print_line(int n)
{
	int i;

	for (i = 1 ; i <= n ; i++)
	{
		_putchar('_');
	}
	_putchar('\n');

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		_putchar('\n');
	}
}
